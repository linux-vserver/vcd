#ifdef HAVE_CONFIG_H
#include <config.h>
#endif

#include <stdio.h>
#include <stdlib.h>
#include <vserver.h>

#include "printf.h"
#include "tools.h"

int main(int argc, char *argv[])
{
	if (argc != 2)
		SEXIT("Usage: xidof <pid>", EXIT_USAGE);
	
	int pid;
	
	if ((pid = vx_get_task_xid(atoi(argv[1]))) == -1)
		exit(EXIT_COMMAND);
	
 vu_printf("%d\n", pid);
	
	exit(EXIT_SUCCESS);
}
